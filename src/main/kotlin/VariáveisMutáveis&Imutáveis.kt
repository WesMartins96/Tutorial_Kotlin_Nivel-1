fun main() {
    /**Variáveis Mutáveis!
    são variáveis que uma vez declaradas podem ser mudadas.*/
    var nome = "Wesley Martins"
    nome = "Wes Martins"
    println(nome)

    /**Variáveis Imutáveis!
    são váriaveis que uma vez declaradas nunca poderão ser mudadas. é usado "val" em vez de "var"*/
    val idade = 10
    println("Tenho $idade anos")
    /* se tentar fazer uma mudança no "val idade" esse código acima apresentará erro pois quando se usa
    "val" para declarar uma variável nunca poderá ser mudada */

}